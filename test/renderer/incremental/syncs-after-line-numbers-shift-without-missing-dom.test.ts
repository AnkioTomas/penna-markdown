function lineChange(
  fromA: number,
  toA: number,
  fromB: number,
  toB: number,
  deletedLines?: number,
  insertedLines?: number,
): CherryChangeLineSet {
  return {
    fromA,
    toA,
    fromB,
    toB,
    deletedLines: deletedLines ?? toA - fromA,
    insertedLines: insertedLines ?? toB - fromB,
  };
}

it("syncs after line numbers shift without missing-dom", () => {
  const store: Record<string, string> = {};
  vi.stubGlobal("localStorage", {
    getItem: (key: string) => store[key] ?? null,
    setItem: (key: string, value: string) => {
      store[key] = value;
    },
    removeItem: (key: string) => {
      delete store[key];
    },
    clear: () => {
      for (const key of Object.keys(store)) delete store[key];
    },
    key: () => null,
    length: 0,
  });
  const { renderer, mount } = createRenderer();
  renderer.render("line0\n\nline1\n\nline2");

  const result = renderer.render("prefix\n\nline0\n\nline1\n\nline2", [
    lineChange(1, 0, 1, 2),
  ]);
  expect(result.partial).toBe(true);
  expect(mount.children.length).toBe(4);

  renderer.destroy();

  vi.useRealTimers();
  document.body.innerHTML = "";
});
